package spreedly.client.java.exception;

public class HttpHandlingException extends SpreedlyClientException
{

    /**
     * 
     */
    private static final long serialVersionUID = 2094595311823575090L;

    public HttpHandlingException()
    {
        super();
    }

    public HttpHandlingException(Throwable cause)
    {
        super(cause);
    }

    public HttpHandlingException(String message, Throwable cause)
    {
        super(message, cause);
    }

}
